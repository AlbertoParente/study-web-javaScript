console.log(sum(3,4))

// Function declaration
function sum(x, y) {
    return x + y
}

// Function expression
const subtraction = function (x, y) {
    return x - y
}
console.log(subtraction(3,4))

// Named function expression
const multiplication = function multiplication(x ,y) {
    return x * y
}
console.log(multiplication(3,4))

